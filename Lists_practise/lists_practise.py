# ======================= Задание 2 =========================
# С клавиатуры вводится число n, затем n целых чисел, каждое на новой строке.
# Создайте список из этих чисел (1-ое число n в список не включаем). Затем найдите максимум и минимум этого списка,
# не используя функции max и min.

n = int(input("Введите количество чисел: "))
numbers = []

for i in range(n):
    num = int(input())
    numbers.append(num)

print(numbers)


min_num = numbers[0]
max_num = numbers[0]

for num in numbers:
    if num < min_num:
        min_num = num
    if num > max_num:
        max_num = num

print(f"Минимальное число в списке: {min_num}, а максимальное число в списке: {max_num}")


