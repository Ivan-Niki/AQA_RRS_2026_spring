# ======================= Задание 4 =========================
# Дан список numbers_lst = [1, 78, 23, -65, 99, 9089, 34, -32, 0, -67, 1, 11, 111].
# Посчитайте и выведите сумму квадратов каждого числа в этом списке. (Ответ: 82649673)

numbers_lst = [1, 78, 23, -65, 99, 9089, 34, -32, 0, -67, 1, 11, 111]

# ----------- Первый способ ----------
nums_sq = []
for elem in numbers_lst:
    nums_sq.append(elem * elem)

res = sum(nums_sq)
print(res)

# ----------- Второй способ ----------
ans = 0
for num in numbers_lst:
    ans += num ** 2

print("Сумма квадратов чисел равна:", ans)

